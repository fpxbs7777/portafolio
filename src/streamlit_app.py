            if not symbols:
                st.error("‚ùå No se encontraron s√≠mbolos v√°lidos para procesar")
                return False
            
            # Obtener datos hist√≥ricos
            data_frames = {}
            
            with st.spinner("Obteniendo datos hist√≥ricos..."):
                for simbolo, mercado in zip(symbols, markets):
                    df = obtener_serie_historica_iol(
                        self.token,
                        mercado,
                        simbolo,
                        self.fecha_desde,
                        self.fecha_hasta
                    )
                    
