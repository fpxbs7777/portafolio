def obtener_serie_historica_iol(token_portador, mercado, simbolo, fecha_desde, fecha_hasta, ajustada="SinAjustar"):
    """
    Obtiene series hist√≥ricas desde la API de IOL
    
    Args:
        token_portador: Token de autenticaci√≥n Bearer
        mercado: Mercado (BCBA, NYSE, NASDAQ, ROFEX)
        simbolo: S√≠mbolo del activo (puede ser string o dict con clave 'simbolo')
        fecha_desde: Fecha inicio (YYYY-MM-DD)
        fecha_hasta: Fecha fin (YYYY-MM-DD)
        ajustada: "Ajustada" o "SinAjustar"
    
    Returns:
        DataFrame con datos hist√≥ricos o None si hay error
    """
    # Manejar caso donde simbolo es un diccionario
    if isinstance(simbolo, dict):
        simbolo = simbolo.get('simbolo', '')
    
    if not simbolo:
        st.warning("No se proporcion√≥ un s√≠mbolo v√°lido")
        return None
        
    # Asegurarse de que el mercado est√© en may√∫sculas
    mercado = mercado.upper() if mercado else 'BCBA'
    try:
        # Construir la URL de la API
        url = f"https://api.invertironline.com/api/v2/{mercado}/Titulos/{simbolo}/Cotizacion/seriehistorica/{fecha_desde}/{fecha_hasta}/{ajustada}"
        headers = {
            'Accept': 'application/json',
            'Authorization': f'Bearer {token_portador}'
        }
        
        response = requests.get(url, headers=headers)
        response.raise_for_status()
        
        data = response.json()
        df = pd.DataFrame(data)
        
        if 'fechaHora' in df.columns:
            # Handle different datetime formats
            df['fecha'] = pd.to_datetime(
                df['fechaHora'], 
                format='mixed',  # Automatically infer format for each element
                utc=True,        # Ensure timezone awareness
                errors='coerce'  # Convert parsing errors to NaT
            ).dt.tz_convert(None).dt.date  # Convert to naive date
            
            # Drop rows where date parsing failed
            df = df.dropna(subset=['fecha'])
            df = df.sort_values('fecha')
            
        return df
        
    except Exception as e:
        st.error(f"Error obteniendo datos para {simbolo}: {str(e)}")
        return None




# --- Portfolio Metrics Function ---
def calcular_metricas_portafolio(portafolio, valor_total):
    """
    Calcula m√©tricas clave de desempe√±o para un portafolio de inversi√≥n.
    
    Args:
        portafolio (dict): Diccionario con los activos y sus cantidades
        valor_total (float): Valor total del portafolio
        
    Returns:
        dict: Diccionario con las m√©tricas calculadas
    """
    if not isinstance(portafolio, dict):
        return {}

    if valor_total == 0:
        return {
            'concentracion': 0,
            'std_dev_activo': 0,
            'retorno_esperado_anual': 0,
            'pl_esperado_min': 0,
            'pl_esperado_max': 0,
            'probabilidades': {'perdida': 0, 'ganancia': 0, 'perdida_mayor_10': 0, 'ganancia_mayor_10': 0},
            'riesgo_anual': 0
        }

    # 1. Calcular concentraci√≥n del portafolio
    concentracion = 0
    for activo in portafolio.values():
        concentracion += (activo.get('Valuaci√≥n', 0) / valor_total) ** 2
    
    # 2. Calcular volatilidad de los activos
    std_dev_activo = 0
    for activo in portafolio.values():
        std_dev_activo += activo.get('Valuaci√≥n', 0) * activo.get('volatilidad', 0)
    
    # 3. Calcular retorno esperado
    retorno_esperado_anual = 0
    for activo in portafolio.values():
        retorno_esperado_anual += activo.get('Valuaci√≥n', 0) * activo.get('retorno_esperado', 0)
    
    # 4. Calcular escenarios de p√©rdida y ganancia
    pl_esperado_min = 0
    pl_esperado_max = 0
    for activo in portafolio.values():
        pl_esperado_min += activo.get('Valuaci√≥n', 0) * activo.get('pl_min', 0)
        pl_esperado_max += activo.get('Valuaci√≥n', 0) * activo.get('pl_max', 0)
    
    # 5. Calcular probabilidades de p√©rdida y ganancia
    probabilidades = {
        'perdida': 0,
        'ganancia': 0,
        'perdida_mayor_10': 0,
        'ganancia_mayor_10': 0
    }
    for activo in portafolio.values():
        probabilidades['perdida'] += activo.get('Valuaci√≥n', 0) * activo.get('probabilidad_perdida', 0)
        probabilidades['ganancia'] += activo.get('Valuaci√≥n', 0) * activo.get('probabilidad_ganancia', 0)
        probabilidades['perdida_mayor_10'] += activo.get('Valuaci√≥n', 0) * activo.get('probabilidad_perdida_mayor_10', 0)
        probabilidades['ganancia_mayor_10'] += activo.get('Valuaci√≥n', 0) * activo.get('probabilidad_ganancia_mayor_10', 0)
    
    # 6. Calcular riesgo anual
    riesgo_anual = 0
    for activo in portafolio.values():
        riesgo_anual += activo.get('Valuaci√≥n', 0) * activo.get('riesgo', 0)
    
    return {
        'concentracion': concentracion,
        'std_dev_activo': std_dev_activo,
        'retorno_esperado_anual': retorno_esperado_anual,
        'pl_esperado_min': pl_esperado_min,
        'pl_esperado_max': pl_esperado_max,
        'probabilidades': probabilidades,
        'riesgo_anual': riesgo_anual
    }

