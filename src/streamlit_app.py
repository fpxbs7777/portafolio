class PortfolioOptimizer:
    def __init__(self, username: str, password: str):
        self.client = IOLClient(username, password)
        self.market_data = MarketDataFetcher(self.client)
        self.portfolio_builder = PortfolioBuilder(self.market_data)
        self.technical_indicators = TechnicalIndicators()
        
    def get_portfolio_data(
        self,
        panels: List[str],
        max_assets: int = 10,
        min_price: float = 0,
        max_price: float = float('inf'),
        start_date: str = '2024-01-01',
        end_date: str = None
    ) -> Dict:
        """Get portfolio data with historical prices and analysis"""
        if not end_date:
            end_date = datetime.now().strftime('%Y-%m-%d')
            
        portfolio = self.portfolio_builder.build_random_portfolio(
            panels=panels,
            max_assets=max_assets,
            min_price=min_price,
            max_price=max_price
        )
        
        if not portfolio['assets']:
            return None
            
        analysis = self.portfolio_builder.analyze_portfolio(
            portfolio=portfolio,
            start_date=start_date,
            end_date=end_date
        )
        
        return {
            'portfolio': portfolio,
            'analysis': analysis
        }
    
    def plot_portfolio_analysis(self, portfolio_data: Dict):
        """Plot portfolio analysis results"""
        if not portfolio_data or not portfolio_data['analysis']['assets']:
            st.warning("No hay datos para graficar")
            return
            
        # Get first asset for technical analysis
        symbol = portfolio_data['analysis']['assets'][0]['symbol']
        prices = self.market_data.get_historical_data(
            symbol=symbol,
            market='BCBA',
            start_date='2024-01-01',
            end_date=datetime.now().strftime('%Y-%m-%d')
        )
        
        if prices is not None and not prices.empty:
            # Calculate indicators
            prices['RSI'] = self.technical_indicators.calculate_rsi(prices['close'])
            macd = self.technical_indicators.calculate_macd(prices['close'])
            
            # Create plot
            fig = make_subplots(rows=3, cols=1, shared_xaxes=True)
            
            # Price chart
            fig.add_trace(
                go.Scatter(
                    x=prices.index,
                    y=prices['close'],
                    name='Precio'
                ),
                row=1, col=1
            )
            
            # RSI
            fig.add_trace(
                go.Scatter(
                    x=prices.index,
                    y=prices['RSI'],
                    name='RSI'
                ),
                row=2, col=1
            )
            fig.add_hline(y=70, line_dash="dash", row=2, col=1)
            fig.add_hline(y=30, line_dash="dash", row=2, col=1)
            
            # MACD
            fig.add_trace(
                go.Scatter(
                    x=prices.index,
                    y=macd['macd'],
                    name='MACD'
                ),
                row=3, col=1
            )
            fig.add_trace(
                go.Scatter(
                    x=prices.index,
                    y=macd['signal'],
                    name='Signal'
                ),
                row=3, col=1
            )
            
            st.plotly_chart(fig, use_container_width=True)

